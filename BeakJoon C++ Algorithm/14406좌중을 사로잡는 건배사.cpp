/// 14406좌중을 사로잡는 건배사

#include <iostream>
#include <string>

using namespace std;

int main()
{
	ios::sync_with_stdio(false);
	cin.tie(0);

	string call;
	cin >> call;
	if			(call == "사.우.나")
		cout << "사랑과 우정을 나누자";
	else if		(call == "오.징.어")
		cout << "오래도록 징그럽게 어울리자";
	else if		(call == "사.이.다")
		cout << "사랑하자 이 세상 다 바쳐";
	else if		(call == "나.가.자")
		cout << "나라, 가정, 자신의 발전을 위하여";
	else if		(call == "재.개.발")
		cout << "재미있고 개성있게 발전적으로 살자";
	else if		(call == "우.아.미")
		cout << "우아하고 아름다운 미래를 위하여";
	else if		(call == "이.기.자")
		cout << "이런 기회를 자주 만들자";
	else if		(call == "청.바.지")
		cout << "청춘은 바로 지금부터";
	else if		(call == "걸.걸.걸")
		cout << "더 사랑할걸, 더 참을걸, 더 즐길걸.";//9
	else if		(call == "지.화.자")
		cout << "지금부터 화합하자";
	else if		(call == "해.당.화")
		cout << "해가 갈수록 당당하고 화려하게";
	else if		(call == "주.전.자")
		cout << "주인의식을 갖고 전문성을 갖추고 자신있게 살자";
	else if		(call == "재.건.축")
		cout << "재미있고 건강하게, 축복하며 살자";
	
}